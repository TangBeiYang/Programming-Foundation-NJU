// ========================================
// 1. 链表节点结构体定义
// ========================================
struct ListNode {
    int val;                // 节点值
    ListNode* next;         // 指向下一个节点的指针
    // 构造方法
    ListNode() : val(0), next(nullptr) {}                    // 默认构造函数
    ListNode(int x) : val(x), next(nullptr) {}               // 带值构造函数
    ListNode(int x, ListNode* next) : val(x), next(next) {}  // 全参构造函数
};
// 使用示例：
ListNode example = ListNode();  // 使用默认构造方法创建结构体实例

// ========================================
// 2. 学生结构体定义
// ========================================
enum FeMale {F, M};  // 枚举类型，F=0, M=1
struct Stu {
    int id;            // 4字节
    char name;         // 1字节（结构体内存分配以4字节为单位，不足4的按4计算）
    FeMale s;          // 枚举类型（通常为4字节）
    int age;           // 4字节
    float score;       // 4字节
    // 注意：若有两个char，放在相邻位置可以更省内存
};

// 获取结构体大小
sizeof(Stu);  // 值为20

// ========================================
// 内存对齐规则说明：
// ========================================
// 1. 结构体内存分配以4字节为单位，不足4的按4计算
// 2. 若有两个char类型成员，放在相邻位置可以节省内存
// 3. 实际大小取决于编译器的内存对齐规则
